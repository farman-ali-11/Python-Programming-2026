# Simple_Naming_Program

a = input("Enter your name: ")

if ( a == "farman"):
    print(f"{a} is a good boy..")

elif (a == "kashif"):
    print(f"{a} is not a good boy..")

elif (a == "ahmed"):
    print(f"{a} is a genius..")

else:
    print(f"{a} is a bad boy... ")


