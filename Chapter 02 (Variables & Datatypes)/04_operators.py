# Arthmetic Operators
a = 10
b = 3

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b) # Floor division
print(a%b) # Modulo operator
print(a**b) # Exponentiation operator

# Assignment Operators
x = 5-3 # assign 5-3 in x
print(x)
y = 10
# y += 3 # Increment the value of y by 3 and then assign it to y
y -= 3 # Decrement the value of y by 3 and then assign it to y
print(y)


# Comparison Operators
f = 5
r = 3
print(f == r) # Equal to
print(f != r) # Not equal to
print(f > r) # Greater than
print(f < r) # Less than
print(f >= r) # Greater than or equal to
print(f <= r) # Less than or equal to

# Logical Operators

# Truth table for 'OR'
print("True or False:", True or False) # True
print("True or True:", True or True) # True
print("False or False:", False or False) # False
print("False or True:", False or True) # True


# Truth table for 'AND'
print("True and False:", True and False) # False
print("True and True:", True and True) # True
print("False and False:", False and False) # False
print("False and True:", False and True) # False

# Truth table for 'NOT'
print(not(True)) # False
print(not(False)) # True





